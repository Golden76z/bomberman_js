// Variable that contains all the maps in an array
export let maps = [MAP_1, MAP_2, MAP_3];

// Function to update a specific map
export function updateMap(mapIndex, newMapData) {
  if (mapIndex < 0 || mapIndex >= maps.length) {
    throw new Error('Invalid map index');
  }
  maps[mapIndex] = newMapData;
}

// Function to update the map variable for other files
export function updateMultipleMaps(updates) {
  updates.forEach(({ mapIndex, newMapData }) => {
    updateMap(mapIndex, newMapData);
  });
}

